# Проверка наличия трёх аргументов
if [ $# -ne 3 ]; then
  echo "Ошибка: требуется три аргумента - первое число, оператор (+, -, \*, /) и второе число."
  exit 1
fi

# Присваиваем аргументы переменным
num1=$1
operator=$2
num2=$3

# Проверяем, что первый и третий аргументы — целые числа
if ! [[ "$num1" =~ ^-?[0-9]+$ ]] || ! [[ "$num2" =~ ^-?[0-9]+$ ]]; then
  echo "Ошибка: аргументы 1 и 3 должны быть целыми числами."
  exit 1
fi

# Выполняем соответствующую операцию
case $operator in
  +)
    result=$((num1 + num2))
    ;;
  -)
    result=$((num1 - num2))
    ;;
  \*)
    result=$((num1 * num2))
    ;;
  /)
    # Проверка деления на ноль
    if [ "$num2" -eq 0 ]; then
      echo "Ошибка: деление на ноль недопустимо."
      exit 1
    fi
    result=$((num1 / num2))
    ;;
  *)
    echo "Ошибка: недопустимый оператор. Используйте один из следующих: +, -, *, /."
    exit 1
    ;;
esac

# Вывод результата
echo "Результат: $result"
